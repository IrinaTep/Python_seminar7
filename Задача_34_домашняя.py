# Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм. 
# Поскольку разобраться в его кричалках не настолько просто, насколько легко он их придумывает, Вам стоит написать программу. 
# Винни-Пух считает, что ритм есть, если число слогов (т.е. число гласных букв) в каждой фразе стихотворения одинаковое. Фраза может состоять из одного
# слова, если во фразе несколько слов, то они разделяются дефисами. Фразы отделяются другот друга пробелами. Стихотворение Винни-Пух вбивает в программу с клавиатуры. 
# В ответе напишите “Парам пам-пам”, если с ритмом все в порядке и “Пам парам”, если с ритмом все не в порядке.
# Ввод:                                                        Вывод:
# пара-ра-рам рам-пам-папам па-ра-па-дам                       Парам пам-пам

def check_rhythm(poem):
    syllables = lambda phrase: sum(1 for letter in phrase.lower() if letter in 'аеёиоуыэюя')
    syllable_counts = [syllables(phrase) for phrase in poem.split()]
    return "Парам пам-пам" if len(set(syllable_counts)) == 1 else "Пам парам"

poem = input("Введите стихотворение Винни-Пуха: ")
result = check_rhythm(poem)
print(result)

