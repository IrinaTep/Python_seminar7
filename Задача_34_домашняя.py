def rhythm(str):
    str = str.lower().split()
    list = []
    for word in str:
        result = 0
        for i in word:
            if i in 'аеёиоуыэюя':
                result += 1
        list.append(result)
    return len(list) == list.count(list[0]) # сравнивает количество одного элемента с длиной списка

